function highlight(content::String)
    panel = Panel(
        content,
        justify          = :left,
        style            = "gold1",
        title            = "🌺 Welcome to Art Gallery 🌺",
        title_style      = "bold cyan centre",
        title_justify    = :center,
        subtitle         = string(today()),
        subtitle_style   = "bold cyan",
        subtitle_justify = :right,
        width            = 79
    )

    return print(panel)
end